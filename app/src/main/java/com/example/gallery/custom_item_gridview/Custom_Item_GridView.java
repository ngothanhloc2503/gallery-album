package com.example.gallery.custom_item_gridview;

public class Custom_Item_GridView {
    private int id;
    private String name;

    public Custom_Item_GridView(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
